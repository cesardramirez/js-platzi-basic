// Operadores Aritméticos.
3 + 2
50 - 10
10 * 20
20 / 2

// Concatenación de Strings
"Cesar " + "Ramírez"

// Negación
!false  // true

// Asignación
var a = 1

// Comparación
3 == "3"   // true : No compara el tipo de dato, sino el valor.
3 === "3"  // false : Compara tanto el tipo de dato como el valor.
5 > 3
5 < 3
4 <= 8
5 >= 6
// Comparadores lógicos
a && b  // Las dos condiciones deben ser verdaderas.
a || b  // Alguna de las dos condiciones debe ser verdadera.

// Incremento
var age = 40
age++  // 40 : Cuando se vuelva a llamar tendrá el valor 41.
age += 3 // 44 : A la variable actual le suma 3.